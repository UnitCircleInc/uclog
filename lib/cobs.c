void dumphex(uint8_t* b, size_t n) {
  for (int j = 0; j < n; j++) {
    printf(" %02x", b[j]);
    if ((j+1) %16 == 0) printf("\n");
  }
  printf("\n");
}

int main(void) {
  uint8_t b[1000];

  for (int i = 0; i < (sizeof(tests)/sizeof(*tests)); i++) {
    size_t n = tests[i].n;
    size_t nn = cobs_enc_size(tests[i].n) - tests[i].n;
    memmove(b+nn, tests[i].b, tests[i].n);

    size_t no = cobs_enc(b, b+nn, tests[i].n);
    if (no > nn + tests[i].n) {
      printf("test case %d - failed: no (%zu) > expected (%zu)\n",
          i+1, no, nn + tests[i].n);
      continue;
    }

    no = cobs_dec(b, b, no);
    if (no < 0) {
      printf("test case %d - failed: nout (%zd) < 0\n",
          i+1, no);
      continue;
    }
    if (no != tests[i].n) {
      printf("test case %d - failed: nout (%zu) != nin (%zu)\n",
          i+1, no, tests[i].n);
      continue;
    }

    if (memcmp(b, tests[i].b, tests[i].n) != 0) {
      printf("test case %d - failed: dec != enc\n", i+1);
      continue;
    }
    printf("test case %d - passed\n", i+1);
  }

  for (int i = 0; i < 900; i++) {
    size_t nn = cobs_enc_size(i) - i;

    for (int j = 0; j < i; j++) {
      b[j+nn] =  (j % 255) + 1;
    }
    size_t no = cobs_enc(b, b+nn, i);

    no = cobs_dec(b, b, no);

    if (no > nn + i) {
      printf("test case len %d - failed: no (%zu) > expected (%zu)\n",
          i+1, no, nn + i);
      continue;
    }

    for (int j = 0; j < i; j++) {
      if (b[j] != ((j % 255) +1 )) {
        printf("test case len %d failed\n", i);
        break;
      }
    }
  }

  for (int i = 0; i < (sizeof(dec_tests)/sizeof(*dec_tests)); i++) {
    memmove(b, dec_tests[i].b, dec_tests[i].n);
    ssize_t no = cobs_dec(b, b, dec_tests[i].n);
    if (no != dec_tests[i].nout) {
      printf("dec test case %d - failed: nout (%zd) != expected (%zd)\n",
          i+1, no, dec_tests[i].nout);
      continue;
    }
    printf("dec test case %d - passed\n", i+1);
  }

  return 0;
}

